# ╔═╡ 0c8cfee6-5edb-4400-b7cd-753c2975a1e4
function tmap(fn, itr)
    # for each i ∈ itr, spawn a task to compute fn(i)
    tasks = map(i -> Threads.@spawn(fn(i)), itr)
    # fetch and return all the results
    return fetch.(tasks)
end

